    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("view/signup.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String name = request.getParameter("name");
        String email = request.getParameter("email");
        String id = email.substring(0, email.indexOf("@"));
        String avatar = "https://cdn.dribbble.com/users/49910/screenshots/4431133/attachments/1006783/avatar_03.png";
        String password = request.getParameter("password");
        String date = request.getParameter("date");
        String year = request.getParameter("year");
        String month = request.getParameter("month");
        Date dob = Date.valueOf(year + "-" + month + "-" + date);
        boolean gender;
        if (request.getParameter("gender").equalsIgnoreCase("male")) {
            gender = true;
        }else{
            gender = false;
        }
        User u = new User(id, email, name, password, dob, gender, avatar);
        UserDAO userDao = new UserDAO();
        userDao.insertUser(u);
        response.sendRedirect("login");
        

        //String password = request.getParameter("")
    }

